/**
 * Enumeration of available input box types
 * Each type represents a different kind of PHP element that can be created
 */
export enum InputBoxTypeEnum {
    /** Represents a regular PHP file */
    File = "File",
    /** Represents a PHP class */
    Class = "Class",
    /** Represents a PHP interface */
    Interface = "Interface",
    /** Represents a PHP enum */
    Enum = "Enum",
    /** Represents a PHP trait */
    Trait = "Trait",
}
